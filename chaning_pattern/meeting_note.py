"""
Chaining 패턴
=====================================

목표: 회의록(긴 글)을 → 요약 → 액션 아이템 → 슬랙 메시지로
      3단계에 걸쳐 변환한다.

핵심 아이디어:
  - ask()  = LLM을 1번 호출하는 가장 작은 단위
  - chain() = ask()를 순서대로 여러 번 (= 워크플로우)
  - 1단계 결과가 2단계 입력이 된다. 그게 "체이닝(사슬 연결)"이다.

실행 방법:
  1) pip install anthropic
  2) export ANTHROPIC_API_KEY='sk-ant-...'   (Windows: set ANTHROPIC_API_KEY=...)
  3) python my_first_workflow.py
"""

import os
import sys

# anthropic 라이브러리가 없으면 친절하게 알려주고 종료
try:
    from anthropic import Anthropic
except ImportError:
    sys.exit("먼저 터미널에서 'pip install anthropic' 를 실행하세요.")

# API 키가 없으면 친절하게 알려주고 종료
if not os.environ.get("ANTHROPIC_API_KEY"):
    sys.exit("ANTHROPIC_API_KEY 환경변수가 설정되지 않았습니다.")


# 클라이언트 — 환경변수에서 API 키를 자동으로 읽어온다.
client = Anthropic()


# =============================================================================
# 함수 1) ask — LLM을 딱 1번 호출. 가장 작은 단위.
# =============================================================================
def ask(prompt: str) -> str:
    """프롬프트(글)를 넣으면 LLM의 답(글)을 돌려준다."""
    message = client.messages.create(
        model="claude-haiku-4-5",   # 빠르고 저렴한 모델 (연습용으로 충분)
        max_tokens=1024,            # 응답 최대 길이
        messages=[
            {"role": "user", "content": prompt}
        ],
    )
    # 응답은 항상 '블록의 배열'. 텍스트만 있을 땐 [0].text 면 충분하다.
    return message.content[0].text


# =============================================================================
# 함수 2) chain — ask()를 순서대로 여러 번 호출. 이게 "워크플로우"다.
# =============================================================================
def chain(text: str, steps: list[str]) -> str:
    """
    steps에 담긴 프롬프트들을 순서대로 적용한다.
    각 단계의 '결과'가 다음 단계의 '입력'이 된다.
    """
    result = text
    for i, step_prompt in enumerate(steps, start=1):
        print(f"\n{'─' * 55}")
        print(f"  단계 {i}")
        print(f"{'─' * 55}")

        # 이번 단계의 지시 + (직전 단계의 결과)를 합쳐서 LLM에 전달
        full_prompt = f"{step_prompt}\n\n[입력]\n{result}"
        result = ask(full_prompt)

        print(result)

    return result


# =============================================================================
# 워크플로우의 '단계 정의' — 여기가 우리가 설계하는 부분이다.
# 각 문자열 하나가 한 단계의 '지시(프롬프트)'다.
# =============================================================================
WORKFLOW_STEPS = [
    # 1단계: 회의록을 요약
    "다음 회의록을 5문장 이내로 요약해 줘. 의사결정과 합의 사항 위주로.",

    # 2단계: 요약에서 액션 아이템 추출
    """다음 회의 요약에서 '누가 무엇을 언제까지' 형태의 액션 아이템을 모두 뽑아 줘.
형식: "- [담당자]: [할 일] (~기한)"
기한이 명시되지 않았으면 (기한 미정)이라고 적어.""",

    # 3단계: 슬랙용 친근한 메시지로 변환
    """다음 액션 아이템들을 슬랙에 올릴 친근한 메시지로 변환해 줘.
이모지를 적당히(과하지 않게) 사용하고, 맨 위에 한 줄 인사를 넣어 줘.""",
]


# =============================================================================
# 샘플 입력 — 실제로는 회의 녹취 파일을 읽어오면 된다.
# 지금은 연습이니 글을 직접 박아둔다.
# =============================================================================
SAMPLE_MEETING_NOTES = """
[5월 3주차 제품팀 주간 회의록]

참석자: 고윤정(PM), 최윤지(디자인), 노윤서(개발), 홍수주(마케팅)

논의 내용:
- 신규 온보딩 화면 A/B 테스트 결과 공유. B안이 가입 완료율 12% 높음.
  → B안으로 확정하기로 합의.
- 최윤지가 B안 최종 시안을 다듬어 개발팀에 전달하기로 함. 이번 주 금요일까지.
- 노윤서는 B안 적용 작업을 다음 스프린트(5/27 시작)에 포함시키기로 함.
- 홍수주는 온보딩 개선 관련 사용자 공지 문구 초안을 작성. 다음 주 수요일까지.
- 결제 오류 버그(이슈 #412)는 우선순위 높음. 노윤서가 이번 주 내 처리하기로 함.
- 다음 회의는 5/24 동일 시간.
"""


# =============================================================================
# 실행 — 여기서 모든 게 시작된다.
# =============================================================================
if __name__ == "__main__":
    print("=" * 55)
    print("  원본 입력 (회의록)")
    print("=" * 55)
    print(SAMPLE_MEETING_NOTES)

    # 워크플로우 실행: 회의록 → 요약 → 액션아이템 → 슬랙메시지
    final_result = chain(SAMPLE_MEETING_NOTES, WORKFLOW_STEPS)

    print("\n" + "=" * 55)
    print("  최종 결과 (슬랙 메시지)")
    print("=" * 55)
    print(final_result)
